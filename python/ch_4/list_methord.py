# convert all the list of number into proper sequence.
print('1: sort list.')
num=[1,4,5,7,4,3,6,2,9]
num.sort()
print(num)
# reverse list
print('2: reverse list.')
num.reverse()
print(num)
# append list
print('3: append list.')
num.append(45)
print(num)
# insert list
print('4: insert list.')
num.insert(2,34)
print(num)
# pop list
print('5: pop list.')
num=[1,4,5,7,4,3,6,2,9]
num.pop(2)
print(num)
# remove list
print('6: remove list')
num=[1,4,5,7,4,3,6,2,9]
num.remove(5)
print(num)